    class Assets
    {
        public function __construct()
        {
            add_action( 'login_enqueue_scripts', [$this, 'add_login_styles'], 100 );

            add_action( 'wp_enqueue_scripts', [$this, 'frontend_assets'] );

            //add_filter( 'admin_head', [$this, 'dashboard_assets'], 200 );
            add_action( 'enqueue_block_editor_assets', [$this, 'dashboard_assets'], 100 );
            add_filter( 'block_editor_settings_all', [$this, 'inject_styles'] );
        }

        public function add_login_styles()
        {
            //\WA\App\add_vite_HMR();

            $entry_points = [
                'wa/login' => [
                    'filename' => 'resources/css/login.css',
                ],
                //'wa/fonts/google' => [
                //    'filename' => \WA\Theme\wa_google_font(),
                //    'set_type' => 'css',
                //],
            ];

            \WA\App\enqueue_multiple_files( $entry_points );
        }

        /**
         * Inject scripts into the block editor.
         *
         * @return void
         */
        public function dashboard_assets()
        {
            if (  ! is_admin() || ! get_current_screen()?->is_block_editor() ) {
                return;
            }

            /*$dependencies = json_decode( Vite::content( 'editor.deps.json' ) );

            foreach ( $dependencies as $dependency ) {
            if (  ! wp_script_is( $dependency ) ) {
            wp_enqueue_script( $dependency );
            }
            }*/

            $entry_points = [
                //'wa/fonts/google' => [
                //    'filename' => \WA\Theme\wa_google_font(),
                //    'set_type' => 'css',
                //],
                'wa/editorcss'         => [
                    'filename' => 'resources/css/editor.css',
                ],

                'wa/fonts/fontawesome' => [
                    'filename' => \WA\Theme\wa_fontawesome(),
                    //'crossorigin' => 'anonymous',
                ],
            ];

            // Add editor script
            $handle = 'wa-editor-sync';
            $src    = 'resources/js/editor.jsx';

            $dependencies   = json_decode( Vite::content( 'editor.deps.json' ) );
            $dependencies[] = 'wp-editor';
            $dependencies[] = 'wp-edit-post';
            $dependencies[] = 'wp-edit-site';
            $dependencies[] = 'wp-dom-ready';
            $dependencies[] = 'wp-blocks';

            if ( Vite::isRunningHot() ) {
                foreach ( $dependencies as $dependency ) {
                    if (  ! wp_script_is( $dependency ) ) {
                        wp_enqueue_script( $dependency );
                    }
                }

                echo Vite::withEntryPoints( [
                    $src,
                ] )->toHtml();
            } else {
                $entry_points[$handle] = [
                    'filename'   => $src,
                    //'dependencies' => $dependencies,
                    'add_module' => true,
                ];
            }
            //var_dump( $entry_points );

            $src                   = 'resources/js/modules/iframe.jsx';
            $handle                = 'waIframe';
            $entry_points[$handle] = [
                'filename'   => $src,
                'add_module' => true,
            ];

            $uris = \WA\App\enqueue_multiple_files( $entry_points );

            wp_localize_script( $handle, 'waEditorSyncVars', [
                'script_url' => Vite::isRunningHot() ? Vite::asset( $src ) : $uris[$handle]['uri_static'],
            ] );
        }

        public function frontend_assets()
        {
            $wp_deps = ['wp-dom-ready']; //, 'wp-element', 'wp-data', 'wp-block-editor'

            $entry_points = [
                //'wa/fonts/google' => [
                //    'filename' => \WA\Theme\wa_google_font(),
                //    'set_type' => 'css',
                //],
                'wa/appcss' => [
                    'filename' => 'resources/css/app.css',
                ],
            ];

            if ( Vite::isRunningHot() ) {
                foreach ( $wp_deps as $dependency ) {
                    if (  ! wp_script_is( $dependency ) ) {
                        wp_enqueue_script( $dependency );
                    }
                }

                echo Vite::withEntryPoints( [
                    'resources/js/app.jsx',
                ] )->toHtml();
            } else {
                $entry_points['wa/appjs'] = [
                    'filename'     => 'resources/js/app.jsx',
                    'dependencies' => $wp_deps,
                    'add_module'   => true,
                ];
            }

            \WA\App\enqueue_multiple_files( $entry_points );

            wp_enqueue_script( 'wa/fonts/fontawesome', \WA\Theme\wa_fontawesome(), false, false, [
                'in_footer'   => true,
                'crossorigin' => 'anonymous',
            ] );
        }

        /**
         * Inject styles into the block editor.
         *
         * @return array
         */
        public function inject_styles( $settings )
        {
            $style = Vite::asset( 'resources/css/editor.css' );

            $settings['styles'][] = [
                'css' => Vite::isRunningHot()
                ? "@import url('{$style}')"
                : Vite::content( 'resources/css/editor.css' ),
            ];

            return $settings;
        }
    }

    new Assets();
}
